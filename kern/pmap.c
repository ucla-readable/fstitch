// These variables are set by i386_detect_memory()
static physaddr_t maxpa;	// Maximum physical address
size_t npage;			// Amount of physical memory (in pages)
static size_t basemem;		// Amount of base memory (in bytes)
static size_t extmem;		// Amount of extended memory (in bytes)

// These variables are set in i386_vm_init()
pde_t* boot_pgdir;		// Virtual address of boot time page directory
physaddr_t boot_cr3;		// Physical address of boot time page directory
static char* boot_freemem;	// Pointer to next byte of free mem

struct Page* pages;		// Virtual address of physical page array
static struct Page_list page_free_list;	// Free list of physical pages

// Global descriptor table.
//
// The kernel and user segments are identical(except for the DPL).
// To load the SS register, the CPL must equal the DPL.  Thus,
// we must duplicate the segments for the user and the kernel.
//
struct Segdesc gdt[] =
{
	// 0x0 - unused (always faults -- for trapping NULL far pointers)
	SEG_NULL,

	// 0x8 - kernel code segment
	[GD_KT >> 3] = SEG(STA_X | STA_R, 0x0, 0xffffffff, 0),

	// 0x10 - kernel data segment
	[GD_KD >> 3] = SEG(STA_W, 0x0, 0xffffffff, 0),

	// 0x18 - user code segment
	[GD_UT >> 3] = SEG(STA_X | STA_R, 0x0, 0xffffffff, 3),

	// 0x20 - user data segment
	[GD_UD >> 3] = SEG(STA_W, 0x0, 0xffffffff, 3),

	// 0x28 - tss, initialized in idt_init()
	[GD_TSS >> 3] = SEG_NULL
};

struct Pseudodesc gdt_pd =
{
	0, sizeof(gdt) - 1, (unsigned long) gdt,
};

static int
nvram_read(int r)
{
	return mc146818_read(NULL, r) | (mc146818_read(NULL, r+1)<<8);
}

void
i386_detect_memory(register_t boot_eax, register_t boot_ebx)
{
	const uint32_t max_maxpa = -KERNBASE;
	/* can't use KADDR yet */
	struct multiboot * mb_info = (struct multiboot *) (boot_ebx + KERNBASE);
	
	if(boot_eax == MULTIBOOT_EAX_MAGIC && mb_info->flags & MULTIBOOT_FLAG_MEMORY)
	{
		// MULTIBOOT tells us how many kilobytes there are
		basemem = ROUNDDOWN32(mb_info->mem_lower * 1024, PGSIZE);
		extmem = ROUNDDOWN32(mb_info->mem_upper * 1024, PGSIZE);
	}
	else
	{
		// CMOS tells us how many kilobytes there are
		basemem = ROUNDDOWN32(nvram_read(NVRAM_BASELO)*1024, PGSIZE);
		extmem = ROUNDDOWN32(nvram_read(NVRAM_EXTLO)*1024, PGSIZE);
	}

	// Calculate the maxmium physical address based on whether
	// or not there is any extended memory.  See comment in ../inc/mmu.h.
	if (extmem)
		maxpa = EXTPHYSMEM + extmem;
	else
		maxpa = basemem;

	// Our kernel will crash (after "check_boot_pgdir() succeeded!" is printed)
	// if it sees more than the amount of physical memory supported by
	// KERNBASE's value. Lower maxpa if necessary, to avoid this crashing:
	if (maxpa > max_maxpa)
	{
		printf("Enabling only %dK of %dK physical memory due to KERNBASE.\n",
		       (int)(max_maxpa/1024), (int)(maxpa/1024));
		maxpa = max_maxpa;
		extmem = maxpa - EXTPHYSMEM;
	}

	npage = maxpa / PGSIZE;

	printf("Physical memory: %dK available, ", (int)(maxpa/1024));
	printf("base = %dK, extended = %dK", (int)(basemem/1024), (int)(extmem/1024));
	if(boot_eax == MULTIBOOT_EAX_MAGIC && mb_info->flags & MULTIBOOT_FLAG_MEMORY)
		printf(" (multiboot)");
	printf("\n");
}

